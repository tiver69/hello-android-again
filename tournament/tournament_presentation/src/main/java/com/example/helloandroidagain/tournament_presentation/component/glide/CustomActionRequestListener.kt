package com.example.helloandroidagain.tournament_presentation.component.glide

import android.graphics.drawable.Drawable
import com.bumptech.glide.load.DataSource
import com.bumptech.glide.load.engine.GlideException
import com.bumptech.glide.request.RequestListener
import com.bumptech.glide.request.target.Target

open class CustomActionRequestListener : RequestListener<Drawable> {

    private lateinit var customAction: () -> Unit

    fun setUpCustomAction(action: () -> Unit): RequestListener<Drawable> = also {
        customAction = action
    }

    override fun onLoadFailed(
        e: GlideException?,
        model: Any?,
        target: Target<Drawable>?,
        isFirstResource: Boolean
    ): Boolean {
        customAction()
        return false
    }

    override fun onResourceReady(
        resource: Drawable?,
        model: Any?,
        target: Target<Drawable>?,
        dataSource: DataSource?,
        isFirstResource: Boolean
    ): Boolean = false
}